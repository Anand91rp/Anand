package org.sel.jav.test;

public class DataTypes {

	public static void main(String[] args) {

		//byte
		
		byte age = 24;
		System.out.println("My Age is:"+age);
	
		//short
		
		short empId = 2254;
		System.out.println("Employee Id:"+empId);
		
		//int
		
		int regno = 225477896;
		System.out.println("Reg No:"+regno);
		
		//long
		
		long phno = 9856325789l;
		System.out.println("phone no:"+phno);
		
		//float
		
		float avg = 22.5f;
		System.out.println("Avg is:"+avg);
		
		//double
		
		double salary = 2254.35;
		System.out.println("Salary:"+salary);
		
		//Char
		
		double bname = 'A';
		System.out.println("Block Name:"+bname);
				
		
		//Boolean
		
		Boolean empstat= true;
		System.out.println("Emp Status:"+empstat);
		
		//String
		
		String email = "anandjdj@gmail.com";
		System.out.println("Mail Id:"+email);
			
				
	}

}
